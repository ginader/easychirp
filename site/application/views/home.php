<h1>Easy Chirp 2</h1>

<p>Easy Chirp is a web-accessible alternative to the Twitter.com website. It is designed to be easier to use and is optimized for disabled users. It also works with keyboard-only, older browsers like IE6, lowband internet connection, and without JavaScript. Learn more about web accessibility.
Refer to the development tasks further down this page for items currently in progress. Learn more about Easy Chirp on the About and Features pages. You may also read an extensive list of user feedback and blogs/articles about this application.</p>

