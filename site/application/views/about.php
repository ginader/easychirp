<div class="box1 rounded">
	<h2>The Name</h2>
	<p>A "2" was added to the Easy Chirp name in mid-2013 after a full relaunch (and rewrite). The relaunch was due to major updates needed including a change in the Twitter API. A successful <a href="http://www.kickstarter.com/projects/dlembree/easy-chirp-2-contribute-to-an-inclusive-twittersph">Kickstarter campaign</a> was run in order to help funding for the relaunch.</p>
	<p>Originally, the app was named &quot;Accessible Twitter&quot;. Many of the articles and awards cited on this website use this name. Learn more <a href="http://www.weboverhauls.com/ce/pressreleases/pr_2011_06_01.htm">about the name change</a>.</p>
</div>

